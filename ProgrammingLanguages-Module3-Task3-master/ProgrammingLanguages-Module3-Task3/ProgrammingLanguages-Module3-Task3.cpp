typedef complex<double> Complex;
typedef valarray<Complex> CArray;


void FastFourierTransform(CArray& x) {
	int dataSize = x.size();

	if (dataSize <= 1) {
		return;
	}

	CArray even = x[slice(0, dataSize / 2, 2)];
	CArray odd = x[slice(1, dataSize / 2, 2)];

	FastFourierTransform(even);
	FastFourierTransform(odd);

	for (int i = 0; i < dataSize / 2; ++i) {
		Complex t = polar(1.0, -2 * M_PI * i / dataSize) * odd[i];
		x[i] = even[i] + t;
		x[i + dataSize / 2] = even[i] - t;
	}
}

void FastFourierTransformOMP(CArray& x) {
	int dataSize = x.size();

	if (dataSize <= 1) {
		return;
	}

	CArray even = x[slice(0, dataSize / 2, 2)];
	CArray odd = x[slice(1, dataSize / 2, 2)];

#pragma omp sections
	{
#pragma omp section
		{
			FastFourierTransform(even);
		}
#pragma omp section
		{
			FastFourierTransform(odd);
		}
	}

	for (int i = 0; i < dataSize / 2; ++i) {
		Complex t = polar(1.0, -2 * M_PI * i / dataSize) * odd[i];
		x[i] = even[i] + t;
		x[i + dataSize / 2] = even[i] - t;
	}
}

void FastFourierTransformVectorized(CArray &x) {
	const size_t dataSize = x.size();
	if (dataSize <= 1) return;

	CArray even = x[slice(0, dataSize / 2, 2)];
	CArray odd = x[slice(1, dataSize / 2, 2)];

	FastFourierTransformVectorized(even);
	FastFourierTransformVectorized(odd);

	double tempevenRl[4];
	double tempevenIm[4];

	double temptRl[4];
	double temptIm[4];

	__m256d pointerx1Rl;
	__m256d pointerx1Im;
	__m256d pointerx2Rl;
	__m256d pointerx2Im;

	for (size_t k = 0; k < dataSize / 2; k += 4)
	{
		Complex t;

		for (int i = 0; i + k < dataSize / 2 && i < 4; i++) {
			tempevenRl[i] = even[i + k].real();
			tempevenIm[i] = even[i + k].imag();

			t = polar(1.0, -2 * M_PI * (i + k) / dataSize) * odd[i + k];

			temptRl[i] = t.real();
			temptIm[i] = t.imag();
		}

		__m256d loadedEvenRl = _mm256_load_pd(tempevenRl);
		__m256d loadedEvenIm = _mm256_load_pd(tempevenIm);

		__m256d loadedTRl = _mm256_load_pd(temptRl);
		__m256d loadedTIm = _mm256_load_pd(temptIm);

		pointerx1Rl = _mm256_add_pd(loadedEvenRl, loadedTRl);
		pointerx1Im = _mm256_add_pd(loadedEvenIm, loadedTIm);

		pointerx2Rl = _mm256_sub_pd(loadedEvenRl, loadedTRl);
		pointerx2Im = _mm256_sub_pd(loadedEvenIm, loadedTIm);

		for (int i = 0; i + k < dataSize / 2 && i < 4; i++) {
			x[k + i] = Complex(pointerx1Rl.m256d_f64[i], pointerx1Im.m256d_f64[i]);
			x[k + i + dataSize / 2] = Complex(pointerx2Rl.m256d_f64[i], pointerx2Im.m256d_f64[i]);
		}
	}
}

